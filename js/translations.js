// Language Switcher Functions
let currentLang = localStorage.getItem('genki-lang') || 'bg';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('genki-lang', lang);
  document.documentElement.lang = lang;
  updateContent();
  updateLangSwitcher();
}

function updateContent() {
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[key] && translations[key][currentLang]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        if (el.placeholder) {
          el.placeholder = translations[key][currentLang];
        }
      } else if (el.tagName === 'OPTION') {
        el.textContent = translations[key][currentLang];
      } else {
        // Check if it contains HTML
        if (translations[key][currentLang].includes('<')) {
          el.innerHTML = translations[key][currentLang];
        } else {
          el.textContent = translations[key][currentLang];
        }
      }
    }
  });

  // Update placeholder translations
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[key] && translations[key][currentLang]) {
      el.placeholder = translations[key][currentLang];
    }
  });

  // Update page title
  const titleKey = document.querySelector('title')?.getAttribute('data-i18n');
  if (titleKey && translations[titleKey]) {
    document.title = translations[titleKey][currentLang];
  }
}

function updateLangSwitcher() {
  const switcher = document.getElementById('lang-current');
  if (switcher) {
    switcher.textContent = currentLang === 'en' ? 'EN' : 'BG';
  }

  // Update active state in dropdown
  document.querySelectorAll('.lang-option').forEach(opt => {
    if (opt.getAttribute('data-lang') === currentLang) {
      opt.classList.add('bg-primary-50', 'text-primary-700');
    } else {
      opt.classList.remove('bg-primary-50', 'text-primary-700');
    }
  });
}

function initLanguageSwitcher() {
  // Set initial language
  document.documentElement.lang = currentLang;
  updateContent();
  updateLangSwitcher();

  // Reveal content now that correct language is applied (prevents FOUC)
  document.documentElement.classList.add('i18n-ready');

  // Toggle dropdown
  const langToggle = document.getElementById('lang-toggle');
  const langDropdown = document.getElementById('lang-dropdown');

  if (langToggle && langDropdown) {
    langToggle.addEventListener('click', function(e) {
      e.preventDefault();
      e.stopPropagation();
      langDropdown.classList.toggle('hidden');
    });

    // Close dropdown when clicking outside
    document.addEventListener('click', function(e) {
      if (!langToggle.contains(e.target) && !langDropdown.contains(e.target)) {
        langDropdown.classList.add('hidden');
      }
    });
  }

  // Add click handler for language options (both links and buttons)
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.addEventListener('click', function(e) {
      e.preventDefault();
      e.stopPropagation();
      const lang = this.getAttribute('data-lang');
      if (lang) {
        setLanguage(lang);
        // Close dropdown
        if (langDropdown) {
          langDropdown.classList.add('hidden');
        }
      }
    });
  });
}

// Initialize on DOM ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLanguageSwitcher);
} else {
  initLanguageSwitcher();
}
